#四則演算と優位性
print(11 // 3) #11を3で割った商を整数で返す
print(11 / 3) #11を3で割った商を少数で返す
print(11 % 3) #11を3で割った余りを返す
print(11 ** 3) #11の3乗

# 演算の優先順位は通常の四則演算と変わらず、括弧による優先度も同一
print((2 + 3) * (1 + 2))

# 計算では2進数で計算することに留意
# 少数の場合、循環小数になる可能性があることから2進数→10進数が完全一致しない場合がある
print(2.3 * 3.4) # 循環小数によって正しく計算されない例

# 複素数の演算(pythonでは虚数単位を「j」で表現する)

"""
複素数とは
・実数と虚数を組み合わせたもの
虚数とは
2乗したときに0未満になるもの
虚数単位「i」
i * i = -1
"""

print(1.2 + 3.4j)
print((1 + 2j) * (1 - 2j))